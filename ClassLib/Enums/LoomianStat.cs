namespace Charwiki.ClassLib.Enums;

/// <summary>
/// Represents the different Loomian stats in the game.
/// </summary>
public enum LoomianStat
{
    /// <summary>
    /// Represents an unknown Loomian stat.
    /// This is used when the stat is not specified or recognized.
    /// It is typically used as a default value or when the stat is not applicable.
    /// </summary>
    Unknown,

    /// <summary>
    /// The health stat.
    /// </summary>
    Health,

    /// <summary>
    /// The energy stat.
    /// </summary>
    Energy,

    /// <summary>
    /// The meelee attack stat.
    /// </summary>
    MeleeAttack,

    /// <summary>
    /// The melee defense stat.
    /// </summary>
    MeleeDefense,

    /// <summary>
    /// The ranged attack stat.
    /// </summary>
    RangedAttack,

    /// <summary>
    /// The ranged defense stat.
    /// </summary>
    RangedDefense,

    /// <summary>
    /// The speed stat.
    /// </summary>
    Speed
}